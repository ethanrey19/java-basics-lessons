package main.variables;

public class Variables {
    public static void main(String[] args) {

        boolean a = true; // true or false statement / 1 bit
        byte b = 110; // -128 - 127 / 1 byte
        short c =  7200; // -32,768 - 32,767 / 2 bytes
        int d = 9090; // -2 billion - 2 billion / 4 bytes
        long e = 1938193819; // -9 quintillion - 9 quintillion // 8 bytes

        float j = 8.09414f; // fraction up to 6-7 digits / 4 bytes
        double k = 9.1293183193d; // fraction up to 15 digits / 8 bytes

        char x = 'a'; // single character or letter / 2 bytes
        String y = "Hello"; // a sequence of characters / any

    }
}